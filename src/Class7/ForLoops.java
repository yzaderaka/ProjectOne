package Class7;

public class ForLoops {
    public static void main(String[] args) {
        /*
        Prints number from 0 to 9
         */
        int number = 0;
        while (number < 10) {
            System.out.println(number);
            number++;
        }
        for (int i = 0; i < 10; i++) {
            System.out.println(i);


            }
        for (int x = 1; x < 20; x+=2) {
           System.out.println(x);
        }

        }

    }
