public class WhileLoopTask {
    public static void main(String[] args) {
        int number=0;
        while (number<100){
            System.out.println(number);
            number++;

        }
        int number1=1;
        while (number1<15){
            System.out.println(number1);
            number1+=2;
        }

    }
}
