package Class6;

public class IncrementOperator {
    public static void main(String[] args) {

        int number=10;
        number=number+1;
        System.out.println(number);
        number+=1;
        System.out.println(number);
        number++; //also increments the number +1
        System.out.println(number);

        int number2=20;
        number2--; // уменьшает на 1
        System.out.println(number2);
    }
}
