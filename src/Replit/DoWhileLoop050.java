package Replit;

public class DoWhileLoop050 {
    /*
    Step 1: Initialize the i value as int i=7.
    Step 2: Write a program to print a sequence of numbers starting from 7 to 98.
     */
    public static void main(String[] args) {
        int i=7;
        while (i<99){
            System.out.println(i);
            i+=7;
        }

    }
}
