package Replit;

public class DoWhileLoop051 {
    /*
    Using do while loop print numbers from 10 to 20
    Must not include the number 20 in the output
     */
    public static void main(String[] args) {
        int i=10;
        do {
            System.out.println(i);
            i++;

        }while (i<20);

    }
}
